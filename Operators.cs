// Arithmetic Operators
/*

+ = addition
- = subtraction
* = multiplication
/ = devision
% = modulus

++ = increment
-- = decrement

*/

// Logical Operators
/*

&& = Logical and
|| = Logical or
! = Logical not

*/
// Comparison Operators
/*

== = Equal to
!= = Not equal
> = Greater than
< = less than
>= = Greater than or equal to
<= = Less than or equal to

*/
// Assignment Operators
/*

= (Example:) x = 5 (Same as:) x = 5
+= (Example:) x += 5 (Same as:) x = x + 5
-= (Example:) x -= 5 (Same as:) x = x - 5
*= (Example:) x *= 5 (Same as:) x = x * 5
/= (Example:) x /= 5 (Same as:) x = x / 5
%= (Example:) x %= 5 (Same as:) x = x % 5
&= (Example:) x &= 5 (Same as:) x = x & 5
|= (Example:) x |= 5 (Same as:) x = x | 5
^= (Example:) x ^= 5 (Same as:) x = x ^ 5
>>= (Example:) x >>= 5 (Same as:) x = x >> 5
<<= (Example:) x <<= 5 (Same as:) x = x << 5

*/
